  handleDrop(e) {
    let resolved = false;
    if (this.capturedDraggable) {
      const d = this.capturedDraggable;
      this.capturedDraggable = null;
      const candidates = [];
      if (d.files && Array.isArray(d.files))
        candidates.push(...d.files);
      if (d.file)
        candidates.push(d.file);
      if (d.source && typeof d.source === "object" && d.source.path) {
        candidates.push(d.source);
      }
      for (const f of candidates) {
        if (!(f == null ? void 0 : f.path))
          continue;
        const abstract = this.app.vault.getAbstractFileByPath(f.path);
        if (abstract instanceof import_obsidian3.TFolder) {
          this.addContext({
            type: "folder",
            path: abstract.path,
            name: abstract.name
          });
          resolved = true;
        } else if (abstract instanceof import_obsidian3.TFile) {
          this.addContext({
            type: "file",
            path: abstract.path,
            name: abstract.basename
          });
          resolved = true;
        }
      }
    }
    if (resolved)
      return;
    try {
      const dm = this.app.dragManager;
      if (dm == null ? void 0 : dm.draggable) {
        const d = dm.draggable;
        const files = d.files || (d.file ? [d.file] : []);
        for (const f of files) {
          if (!(f == null ? void 0 : f.path))
            continue;
          const abstract = this.app.vault.getAbstractFileByPath(f.path);
          if (abstract instanceof import_obsidian3.TFolder) {
            this.addContext({
              type: "folder",
              path: abstract.path,
              name: abstract.name
            });
            resolved = true;
          } else if (abstract instanceof import_obsidian3.TFile) {
            this.addContext({
              type: "file",
              path: abstract.path,
              name: abstract.basename
            });
            resolved = true;
          }
        }
      }
    } catch (e2) {
      console.debug("OBSICLAUDE: drop resolve via draggable failed:", e2);
    }
    if (resolved)
      return;
    if (e.dataTransfer) {
      const textData = e.dataTransfer.getData("text/plain");
      if (textData) {
        resolved = this.resolvePathsFromText(textData);
      }
      if (!resolved) {
        const uriData = e.dataTransfer.getData("text/uri-list");
        if (uriData) {
          resolved = this.resolvePathsFromText(uriData);
        }
      }
      if (!resolved) {
        for (const t of e.dataTransfer.types) {
          if (t === "text/plain" || t === "text/uri-list" || t === "Files")
            continue;
          const data = e.dataTransfer.getData(t);
          if (data) {
            resolved = this.resolvePathsFromText(data);
            if (resolved)
              break;
          }
        }
      }
    }
    if (!resolved) {
      new import_obsidian3.Notice(
        "Could not identify items. Try right-click \u2192 Send to chat."
      );
    }
  }
  resolvePathsFromText(text) {
    let resolved = false;
    const lines = text.split("\n").filter(Boolean);
    for (const rawLine of lines) {
      let clean = rawLine.trim();
      let file = this.app.vault.getAbstractFileByPath(clean);
      if (!file) {
        try {
          clean = decodeURIComponent(clean);
          file = this.app.vault.getAbstractFileByPath(clean);
        } catch (e) {
        }
      }
      if (!file) {
        const stripped = clean.replace(/^(file|app):\/\/[^/]*/, "").replace(/^\/+/, "");
        file = this.app.vault.getAbstractFileByPath(stripped);
      }
      if (!file) {
        const filename = clean.split("/").pop();
        if (filename) {
          const nameNoExt = filename.replace(/\.md$/, "");
          const allFiles = this.app.vault.getAllLoadedFiles();
          file = allFiles.find(
            (f) => f.name === filename || f instanceof import_obsidian3.TFile && f.basename === nameNoExt
          ) || null;
        }
      }
      if (file instanceof import_obsidian3.TFolder) {
        this.addContext({
          type: "folder",
          path: file.path,
          name: file.name
        });
        resolved = true;
      } else if (file instanceof import_obsidian3.TFile) {
        this.addContext({
          type: "file",
          path: file.path,
          name: file.basename
        });
        resolved = true;
      }
    }
    return resolved;
  }
